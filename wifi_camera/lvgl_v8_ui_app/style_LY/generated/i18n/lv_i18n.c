// Internal state
static const lv_i18n_language_pack_t * current_lang_pack;
static const lv_i18n_lang_t * current_lang;


/**
* Reset internal state. For testing.
*/
void __lv_i18n_reset(void)
{
  current_lang_pack = NULL;
  current_lang = NULL;
}

/**
* Set the languages for internationalization
* @param langs pointer to the array of languages. (Last element has to be `NULL`)
*/
int lv_i18n_init(const lv_i18n_language_pack_t * langs)
{
  if(langs == NULL) return -1;
  if(langs[0] == NULL) return -1;

  current_lang_pack = langs;
  current_lang = langs[0];     /*Automatically select the first language*/
  return 0;
  }

  /**
  * Change the localization (language)
  * @param l_name name of the translation locale to use. E.g. "en-GB"
  */
  int lv_i18n_set_locale(const char * l_name)
  {
  if(current_lang_pack == NULL) return -1;

  uint16_t i;

  for(i = 0; current_lang_pack[i] != NULL; i++) {
    // Found -> finish
    if(strcmp(current_lang_pack[i]->locale_name, l_name) == 0) {
      current_lang = current_lang_pack[i];
      return 0;
    }
  }

  return -1;
}

static uint16_t __lv_i18n_get_msg_id_by_name(const char * msg_name)
{
  if(msg_name == NULL) return 0xFFFF;

  for(uint16_t i = 0; msg_mapping[i].msg_name != NULL; i++) {
    if(strcmp(msg_mapping[i].msg_name, msg_name) == 0) {
      return msg_mapping[i].msg_id;
    }
  }

  return 0xFFFF; // Not found
}

static const char * __lv_i18n_get_text_core(const lv_i18n_phrase_t * trans, uint16_t msg_id)
{
  uint16_t i;
  for(i = 0; trans[i].translation != NULL; i++) {
    if(trans[i].msg_id == msg_id) {
      /*The msg_id has found. Check the translation*/
      if(trans[i].translation) return trans[i].translation;
    }
  }

  return NULL;
}


/**
* Get the translation from a message name
* @param msg_name message name
* @return the translation of `msg_name` on the set local
*/
const char * lv_i18n_get_text(const char * msg_name)
{
  if(current_lang == NULL) return msg_name;

  uint16_t msg_id = __lv_i18n_get_msg_id_by_name(msg_name);
  if(msg_id == 0xFFFF) return msg_name; // Message not found

  const lv_i18n_lang_t * lang = current_lang;
  const char * txt;

  // Search in current locale
  if(lang->singulars != NULL) {
    txt = __lv_i18n_get_text_core(lang->singulars, msg_id);
    if (txt != NULL) return txt;
  }

  // Try to fallback
  if(lang == current_lang_pack[0]) return msg_name;
  lang = current_lang_pack[0];

  // Repeat search for default locale
  if(lang->singulars != NULL) {
    txt = __lv_i18n_get_text_core(lang->singulars, msg_id);
    if (txt != NULL) return txt;
  }

  return msg_name;
}

/**
* Get the translation from a message name and apply the language's plural rule to get correct form
* @param msg_name message name
* @param num an integer to select the correct plural form
* @return the translation of `msg_name` on the set local
*/
const char * lv_i18n_get_text_plural(const char * msg_name, int32_t num)
{
  if(current_lang == NULL) return msg_name;

  uint16_t msg_id = __lv_i18n_get_msg_id_by_name(msg_name);
  if(msg_id == 0xFFFF) return msg_name; // Message not found

  const lv_i18n_lang_t * lang = current_lang;
  const char * txt;
  lv_i18n_plural_type_t ptype;

  // Search in current locale
  if(lang->locale_plural_fn != NULL) {
    ptype = lang->locale_plural_fn(num);

    if(lang->plurals[ptype] != NULL) {
      txt = __lv_i18n_get_text_core(lang->plurals[ptype], msg_id);
      if (txt != NULL) return txt;
    }
  }

  // Try to fallback
  if(lang == current_lang_pack[0]) return msg_name;
  lang = current_lang_pack[0];

  // Repeat search for default locale
  if(lang->locale_plural_fn != NULL) {
    ptype = lang->locale_plural_fn(num);

    if(lang->plurals[ptype] != NULL) {
      txt = __lv_i18n_get_text_core(lang->plurals[ptype], msg_id);
      if (txt != NULL) return txt;
    }
  }

  return msg_name;
}

static const lv_font_t * __lv_i18n_get_font_by_id(uint16_t font_id)
{
  for(uint16_t i = 0; font_mapping[i].font_ptr != NULL; i++) {
    if(font_mapping[i].font_id == font_id) {
      return font_mapping[i].font_ptr;
    }
  }

  return NULL;
}

/*
* Get the font for a message name
* @param msg_name message name
*/
const lv_font_t * lv_i18n_get_font(const char * msg_name)
{
  if(current_lang == NULL) return NULL;

  uint16_t msg_id = __lv_i18n_get_msg_id_by_name(msg_name);
  if(msg_id == 0xFFFF) return NULL; // Message not found

  const lv_i18n_lang_t * lang = current_lang;
  const lv_font_t * font = NULL;

  // Search in current locale singulars
  if(lang->singulars != NULL) {
    for(uint16_t i = 0; lang->singulars[i].translation != NULL; i++) {
      if(lang->singulars[i].msg_id == msg_id) {
        font = __lv_i18n_get_font_by_id(lang->singulars[i].font_id);
        return font;
      }
    }
  }

  // Search in current locale plurals for type ONE
  if(lang->plurals[LV_I18N_PLURAL_TYPE_ONE] != NULL) {
    for(uint16_t i = 0; lang->plurals[LV_I18N_PLURAL_TYPE_ONE][i].translation != NULL; i++) {
      if(lang->plurals[LV_I18N_PLURAL_TYPE_ONE][i].msg_id == msg_id) {
        font = __lv_i18n_get_font_by_id(lang->plurals[LV_I18N_PLURAL_TYPE_ONE][i].font_id);
        return font;
      }
    }
  }

  // Search in current locale plurals for type OTHER
  if(lang->plurals[LV_I18N_PLURAL_TYPE_OTHER] != NULL) {
    for(uint16_t i = 0; lang->plurals[LV_I18N_PLURAL_TYPE_OTHER][i].translation != NULL; i++) {
      if(lang->plurals[LV_I18N_PLURAL_TYPE_OTHER][i].msg_id == msg_id) {
        font = __lv_i18n_get_font_by_id(lang->plurals[LV_I18N_PLURAL_TYPE_OTHER][i].font_id);
        return font;
      }
    }
  }

  return NULL;
}

/**
* Get the name of the currently used locale.
* @return name of the currently used locale. E.g. "en-GB"
*/
const char * lv_i18n_get_current_locale(void)
{
  if(!current_lang) return NULL;
  return current_lang->locale_name;
}

